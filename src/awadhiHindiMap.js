// Reverse lookup helper: given a transcript word, what Awadhi targets could it match?
const _reverseMap = (() => {
  const map = {};
  for (const [awadhi, hindis] of Object.entries(AWADHI_HINDI_MAP)) {
    for (const hindi of hindis) {
      if (!map[hindi]) map[hindi] = [];
      map[hindi].push(awadhi);
    }
  }
  return map;
})();

// Strip punctuation so 'अहा?' looks up the same as 'अहा'
function stripPunct(s) {
  return s.replace(/[।,\.!?;:]/g, '').trim();
}

export function getHindiEquivalents(awadhiWord) {
  return AWADHI_HINDI_MAP[awadhiWord] ?? AWADHI_HINDI_MAP[stripPunct(awadhiWord)] ?? [];
}

export function getAwadhiOriginals(hindiWord) {
  return _reverseMap[hindiWord] ?? _reverseMap[stripPunct(hindiWord)] ?? [];
}
